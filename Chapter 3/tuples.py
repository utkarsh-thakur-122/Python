tup = (4,6,3,1,3,) #The (,) comma in the end is optional for multiple elements but for single element comma is compulsory otherwise it will change the datatype of the element from tuple
print(type(tup))  #Prints the type of tuple
print (tup)
print (tup.index(6)) #Prints the index of the element
print (tup.count(3)) #Counts the total occurence of element